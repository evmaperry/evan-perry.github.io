function reverseArrayInPlace(array) {
  for (let i = 0; i <= (array.length-1)/2 ; i++){
    let front = array[i];
    let back = array[array.length-1-i]
    array[i]=back;
    array[array.length-1-i] = front;
  }
  return array;
}

