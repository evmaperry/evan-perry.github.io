function range(start, end, step=1) {
  let storageArray = [];
  if (start === end){
    return storageArray;
  }
  if (step>0) {
     for (let i = start; i <= end; i= i+step){
       storageArray.push(i);
     }
  }
  if (step<0){
    for (let i = start; i >= end; i= i+step){
      storageArray.push(i)
    }
  }
   return storageArray;
 }

